package array;

public class Singledimensional2 {
	public static void main(String[] args) {

		// Array Declaration
		int[] arr2 = new int[5];

		// Array Initialization
		arr2[0] = 80;
		arr2[1] = 40;
		arr2[2] = 30;
		arr2[3] = 20;
		arr2[4] = 60;

		// Usage
		for (int i = 0; i <= 4; i++) {
			System.out.println(arr2[i]);
		}
	}

}
