package array;

public class Singledimensional5 {
	public static void main(String[] args) {
		// Array Declaration and Array Initialization
		char[] arr5 = { 'A', 'Z', 'G', 'U', 'Q' };
		// Usage
		System.out.println(arr5[2]);

		System.out.println("----------------------");

		// Usage
		for (int i = 0; i <= arr5.length - 1; i++) {
			System.out.println(arr5[i]);
		}
	}
}
