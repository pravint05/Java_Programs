package variables;

public class Datatypes2
{
	public static void main(String[] args) 
		
	{
		//variable declaration
		byte a;
		short b;
		int c;
		long d;
		float e;
		double f;
		char g;
		String h;
		boolean i;
		boolean j;
		
		
		//variable initialisation
		a=10;						// from -128 to 127
		b=32719;					// from -32768 to 32767
		c=1990201216;				// from -2147483648 to 2147483647
		d=62783983810910578L;		// from -9223372036854775808 to 9223372036854775807
		e=10.25234433f;
		f=26.9845765434561223456789001;
		g='A';
		h="ABC@123";
		i=true;
		j=false;
		
		//Usage
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(i);
		System.out.println(j);
	}	
	
}
